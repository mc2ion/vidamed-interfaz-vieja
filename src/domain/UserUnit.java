package domain;

public class UserUnit {

	private Long userUnitID;
	private String name;
	
	public void setUserUnitID(Long userUnitID) {
		this.userUnitID = userUnitID;
	}
	
	public Long getUserUnitID() {
		return userUnitID;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

}
