package servlet;

import java.io.IOException;

import javax.servlet.ServletException;
import javax.servlet.annotation.WebServlet;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import javax.servlet.http.HttpSession;

import command.CommandExecutor;



/**
 * Servlet implementation class SetPharmacyDischargeServlet
 */
@WebServlet(description = "servlet to log in users", urlPatterns = { "/SetPharmacyDischargeServlet" })
public class SetPharmacyDischargeServlet extends HttpServlet {
	private static final long serialVersionUID = 1L;
	
	public void init() throws ServletException {
		super.init();
		try {
			CommandExecutor.getInstance();
		} catch (Exception e) {
			throw new ServletException(e);
		}
	}
       
    /**
     * @see HttpServlet#HttpServlet()
     */
    public SetPharmacyDischargeServlet() {
        super();
    }

	/**
	 * @see HttpServlet#doGet(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		doPost(request, response);
	}

	/**
	 * @see HttpServlet#doPost(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		try {
			HttpSession session = request.getSession(false);
			Long admissionID = Long.parseLong(request.getParameter("userId"));
			Integer result = (Integer) CommandExecutor.getInstance().executeDatabaseCommand(new command.SetPharmacyDischarge(admissionID));
			String text = "Se ha dado dado de alta exitosamente.";
			if (result == 0)
				text = "Hubo un error al dar de alta. Por favor, intente nuevamente.";
			session.setAttribute("info",text);
			
			response.sendRedirect(request.getContextPath() + "/ListPharmacyDischargesServlet");
		}
		catch (Exception e) {
			throw new ServletException(e);
		}
	}
}
